# Ввод координат первой клетки
x1 = int(input("Введите x первой клетки: "))
y1 = int(input("Введите y первой клетки: "))
# Ввод координат второй клетки
x2 = int(input("Введите x второй клетки: "))
y2 = int(input("Введите y второй клетки: "))
# Определяем цвет клетки (1,1) - белая
# Цвет определяется суммой координат: четная сумма - один цвет, нечетная - другой
color1 = (x1 + y1) % 2
color2 = (x2 + y2) % 2
# Проверяем, одного ли цвета клетки
if color1 == color2:
    print("YES")
    # Определяем конкретный цвет
    if color1 == 0:
        print("White")
    else:
        print("Black")
else:
    print("NO")